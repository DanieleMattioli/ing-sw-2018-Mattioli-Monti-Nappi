package it.polimi.se2018;

public class Position
{
    private final int x;
    private final int y;

    //Constructor
    public Position(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    //Getter
    public int getX()
    {
        return this.x;
    }

    public int getY()
    {
        return this.y;
    }
}
